package GsonDataBase;

public class Good {
    String pricePerUnit;
    String purchasePricePerUnit;
    int quantityAvailable;
    int sellPricePerUnit;
    int spread;
    String symbol;
    int volumePerUnit;

    public String getPricePerUnit() {
        return pricePerUnit;
    }

    public String getPurchasePricePerUnit() {
        return purchasePricePerUnit;
    }

    public int getQuantityAvailable() {
        return quantityAvailable;
    }

    public int getSellPricePerUnit() {
        return sellPricePerUnit;
    }

    public int getSpread() {
        return spread;
    }

    public String getSymbol() {
        return symbol;
    }

    public int getVolumePerUnit() {
        return volumePerUnit;
    }
}
