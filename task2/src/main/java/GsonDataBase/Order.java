package GsonDataBase;

public class Order {

    String good;
    int pricePerUnit;
    int quantity;
    int total;

    public String getGood() {
        return good;
    }

    public int getPricePerUnit() {
        return pricePerUnit;
    }

    public int getQuantity() {
        return quantity;
    }

    public int getTotal() {
        return total;
    }
}
